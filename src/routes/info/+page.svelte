    <div class="md:col-span-2 mb-5">
        <h1>What is Raffle Tag Team?</h1>
        <p>The SnapLAN Raffle Tag Team is a competition in which random teams compete against each other in different
            rounds
            to put together the highest scores on the chosen maps with which the team members earn tickets. After each
            round, the team members receive the tickets of the team and thus get a position on the total leaderboard!
            The
            more tickets, the more chance you have of winning a Discord Nitro!
        </p>
    </div>
    <div>
        <h2>Round</h2>
        <p>A round has a start and end date (and time). Each round has different maps than the previous one. You will
            also
            be
            in a team with someone else to battle for the tickets. At the end of the round, you and your teammate
            receive
            the
            tickets you earned as a team.</p>
    </div>
    <div>
        <h2>Maps</h2>
        <p>You will have to play a difficulty of the maps to be able to set a score for your team. If the difficulty is
            too
            high, try to
            set
            a score with No Fail enabled to have a score that counts for your team! (ScoreSaber is needed to set these
            scores!)
        </p>
    </div>
    <div>
        <h2>Team mate</h2>
        <p>You will have a new team mate each round. This way you'll have a better chance to play with the cool Unlucky
            guy!
            It's also best to contact your team mate for that round so they can help you if needed, or to get to know
            new
            people! If you don't know who is
            who, don't hesitate to ask an admin or an other participant!</p>
    </div>
    <div>
        <h2>Tickets</h2>
        <p>A ticket gives you a chance to be picked out of a raffle and win the prize! If your team is first when a
            round
            ends, you get "the
            number of teams * 3" of tickets. If you are second, you will get "(the number of teams - 1) * 3" and so on
            until
            the
            last team gets 3 tickets.</p>
    </div>
    <div>
        <h2>The raffle</h2>
        <p>On Sunday, at the end of the LAN, we start the raffle. There will be a draw for a Discord Nitro and a Discord
            Nitro
            Classic subscription!</p>
    </div>
    <div>
        <h2>History</h2>
        <p>The past editions of SnapLAN always had a Tag Team competition. You may also have noticed that from BBSC we
            also
            organize a Raffle competition every year but it didn't happen this year. So we thought it would be nice to
            combine
            the two into one competition. And we call it Raffle Tag Team!</p>
    </div>
</article>

